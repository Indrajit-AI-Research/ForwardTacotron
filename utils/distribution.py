        y_hat_s, y_hat_p = self.stft.melspectrogram(y_hat.squeeze())
        y_s, y_p = self.stft.melspectrogram(y.squeeze())
        loss_s = F.l1_loss(y_hat_s, y_s)
        loss_p =  F.l1_loss(y_hat_p, y_p)
